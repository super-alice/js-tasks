/**
 * Спросите имя пользователя с помощью методы prompt.
 * Выведите с помощью alert сообщение 'Ваше имя %имя%'.
 */
const name = prompt ('Как вас зовут?','Alise');
alert('Ваше имя '+ name);
/**
 * Спросите у пользователя число. Выведите с помощью alert квадрат этого числа.
 */
const num = prompt ('Запишите число', 10);
alert(num * num);
